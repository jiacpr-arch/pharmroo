async function run() {
  const client = new Client({
    connectionString: DATABASE_URL,
    ssl: { rejectUnauthorized: false },
  });
  await client.connect();

  const subjectRows = await client.query(
    "SELECT id, name FROM mcq_subjects WHERE exam_type = 'NLE'"
  );
  const subjectIdByName = Object.fromEntries(
    subjectRows.rows.map((r) => [r.name, r.id])
  );

  const missing = [...new Set(all.map((q) => q.subjectName))].filter(
    (n) => !subjectIdByName[n]
  );
  if (missing.length > 0) {
    console.error("ERROR: missing nursing subjects:", missing.join(", "));
    process.exit(1);
  }

  let inserted = 0;
  const perSubject = {};
  for (const item of all) {
    const subjectId = subjectIdByName[item.subjectName];
    const id = randomUUID();
    await client.query(
      `INSERT INTO mcq_questions
       (id, subject_id, exam_type, scenario, choices, correct_answer,
        explanation, detailed_explanation, difficulty, is_ai_enhanced,
        ai_notes, status)
       VALUES ($1, $2, 'NLE', $3, $4, $5, $6, $7, $8, false, $9, 'active')`,
      [
        id,
        subjectId,
        item.scenario,
        JSON.stringify(item.choices),
        item.correct_answer,
        item.explanation,
        JSON.stringify(item.detailed_explanation),
        item.difficulty,
        "NLE batch 2 — manual seed, review before high-stakes use",
      ]
    );
    inserted++;
    perSubject[item.subjectName] = (perSubject[item.subjectName] || 0) + 1;
  }

  // Refresh question_count per subject
  await client.query(`
    UPDATE mcq_subjects s
    SET question_count = (
      SELECT COUNT(*) FROM mcq_questions q
      WHERE q.subject_id = s.id AND q.status = 'active'
    )
    WHERE s.exam_type = 'NLE'
  `);

  console.log("=== Per-subject inserted ===");
  for (const [name, count] of Object.entries(perSubject)) {
    console.log(`  ${name}: ${count}`);
  }
  console.log(`\n=== Done: inserted ${inserted} NLE questions ===`);
  await client.end();
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
