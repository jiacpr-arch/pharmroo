const LABELS = ["A", "B", "C", "D"];

function q(
  subjectName,
  scenario,
  choices,
  correct,
  summary,
  reason,
  choiceExp,
  key,
  difficulty = "medium"
) {
  return {
    subjectName,
    scenario,
    choices: choices.map((text, i) => ({ label: LABELS[i], text })),
    correct_answer: correct,
    explanation: summary,
    detailed_explanation: {
      summary,
      reason,
      choices: choices.map((text, i) => ({
        label: LABELS[i],
        text,
        is_correct: correct === LABELS[i],
        explanation: choiceExp[i],
      })),
      key_takeaway: key,
    },
    difficulty,
  };
}

